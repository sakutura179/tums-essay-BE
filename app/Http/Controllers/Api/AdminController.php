    public function auth(Request $request)
    {
        $data = ['username' => $request->username, 'password' => $request->password];
        if (Auth::guard('admin')->attempt($data)) {
            return response()->json([
                'status' => 'success',
                // random token number
                'token' => \Str::random(20)
            ]);
        }
        return response()->json([
            'status' => 'error',
            'message' => 'Incorrect username or password'
        ]);
    }

